#include "aes/aes.h"
#include <stdio.h>


int main(int argc, char const *argv[])
{
    uint8_t in[] = {0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff};
    const uint8_t k[] = {0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c};
    uint8_t out[16] = {0};

    aes128 *aes = aes128_init(k);
    aes128_block(in, out, aes);

    for(int i=0; i<16; ++i)
        printf("%02x", out[i]);
    printf("\n");
    return 0;
}
